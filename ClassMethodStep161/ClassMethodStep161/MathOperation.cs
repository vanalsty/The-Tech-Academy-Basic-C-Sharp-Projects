using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ClassMethodStep161
{
    class MathOperation
    {
        public static int Add(int num)
        {
            return num + 2;
        }
        public static int Mult(int num)
        {
            return num * 2;
        }
        public static int Subtract(int num)
        {
            return num - 2;
        }
        public static int Divide(int num)
        {
            return num / 2;
        }
    }
}
